#include <libft.h>

void	ft_putnbr(int i)
{
	unsigned int nb;

	nb = 0;
	(NEG(i) ? (nb = -i), ft_putchar('-') : (nb = i));
	if (nb > 9)
		ft_putnbr(nb / 10);
	ft_putchar((nb % 10) + 48);
}
