char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int j;

	j = -1;
	while (s1[++j]);
	i = -1;
	while (s2[++i])
		s1[j + i] = s2[i];
	s1[j + i] = s2[i];
	return (s1);
}
