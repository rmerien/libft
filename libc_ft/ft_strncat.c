char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int j;

	j = -1;
	while (s1[++j]);
	i = -1;
	while (s2[++i] && i < (int)n)
		s1[j + i] = s2[i];
	s1[j + i] = s2[i];
	return (s1);
}
